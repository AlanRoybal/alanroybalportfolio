// Converts charm.mov frames into small ASCII sprite frames for ScrollCharm.
// Background (corner-sampled dark purple) is masked out; density is driven by
// distance from that background so the pink charm reads as a white silhouette.
import sharp from "sharp";
import { readdirSync, writeFileSync } from "fs";

const DIR = process.argv[2];
const COLS = 50;
const RAMP = "@%#*+=~:-."; // dense -> faint
const files = readdirSync(DIR).filter((f) => f.endsWith(".png")).sort();

async function toAscii(file) {
  const meta = await sharp(`${DIR}/${file}`).metadata();
  const ROWS = Math.round(COLS * (meta.height / meta.width) * 0.5);
  const { data, info } = await sharp(`${DIR}/${file}`)
    .resize(COLS, ROWS, { fit: "fill" })
    .removeAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true });
  const px = (x, y) => {
    const i = (y * info.width + x) * info.channels;
    return [data[i], data[i + 1], data[i + 2]];
  };
  const corners = [px(0, 0), px(COLS - 1, 0), px(0, ROWS - 1), px(COLS - 1, ROWS - 1)];
  const bg = corners.reduce((a, c) => [a[0] + c[0] / 4, a[1] + c[1] / 4, a[2] + c[2] / 4], [0, 0, 0]);
  const lines = [];
  for (let y = 0; y < ROWS; y++) {
    let line = "";
    for (let x = 0; x < COLS; x++) {
      const [r, g, b] = px(x, y);
      const d = Math.hypot(r - bg[0], g - bg[1], b - bg[2]);
      if (d < 48) { line += " "; continue; }
      const density = Math.min(1, (d - 48) / 120);
      line += RAMP[Math.round((1 - density) * (RAMP.length - 1))];
    }
    lines.push(line.replace(/\s+$/, ""));
  }
  return lines;
}

let frames = [];
for (const f of files) frames.push(await toAscii(f));
// trim shared empty top/bottom rows across all frames
const rowsN = Math.max(...frames.map((f) => f.length));
frames = frames.map((f) => { while (f.length < rowsN) f.push(""); return f; });
const rowHasInk = (i) => frames.some((f) => (f[i] || "").trim() !== "");
let top = 0; while (top < rowsN && !rowHasInk(top)) top++;
let bot = rowsN - 1; while (bot > top && !rowHasInk(bot)) bot--;
frames = frames.map((f) => f.slice(top, bot + 1));
const W = Math.max(...frames.flatMap((f) => f.map((l) => l.length)));
const ROWSF = frames[0].length;

if (process.argv.includes("--preview")) {
  console.log(`frames=${frames.length} ${W}x${ROWSF}`);
  console.log(frames[Math.floor(frames.length / 2)].join("\n"));
} else {
  const out =
    `// AUTO-GENERATED by scripts/gen-charm-sprite-from-video.mjs — ${frames.length} ASCII frames\n` +
    `// the "charm" (pink hearts) from charm.mov, rasterised to white ASCII for ScrollCharm.\n` +
    `export const charmSpriteCols = ${W};\n` +
    `export const charmSpriteRows = ${ROWSF};\n` +
    `export const charmSpriteFrames: string[] = [\n` +
    frames.map((fr) => "  `\n" + fr.join("\n") + "\n`").join(",\n") +
    `\n];\n`;
  writeFileSync("src/content/charmSprite.ts", out);
  console.log(`wrote src/content/charmSprite.ts (${frames.length} frames, ${W}x${ROWSF})`);
}
