// Converts the extracted video frames into ASCII frames. Flat background is
// masked out (corner-sampled), and density is driven by how far each pixel is
// from that background, so the figure reads as a solid silhouette with shading
// on the dark site.
import sharp from "sharp";
import { readdirSync, writeFileSync } from "fs";

const DIR = "/private/tmp/claude-501/-Users-alan-Projects-portfolio/43d5ca22-41ee-43c8-b890-072206d448e3/scratchpad/frames";
const COLS = 150;
const RAMP = "@%#*+=~:-."; // dense -> faint
const SRC_W = 1280;
const SRC_H = 720;
const ROWS = Math.round(COLS * (SRC_H / SRC_W) * 0.5);

const files = readdirSync(DIR).filter((f) => f.endsWith(".png")).sort();
const previewOnly = process.argv.includes("--preview");

async function toAscii(file) {
  const { data, info } = await sharp(`${DIR}/${file}`)
    .resize(COLS, ROWS, { fit: "fill" })
    .removeAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true });
  const px = (x, y) => {
    const i = (y * info.width + x) * info.channels;
    return [data[i], data[i + 1], data[i + 2]];
  };
  const corners = [px(0, 0), px(COLS - 1, 0), px(0, ROWS - 1), px(COLS - 1, ROWS - 1)];
  const bg = corners.reduce((a, c) => [a[0] + c[0] / 4, a[1] + c[1] / 4, a[2] + c[2] / 4], [0, 0, 0]);

  const lines = [];
  for (let y = 0; y < ROWS; y++) {
    let line = "";
    for (let x = 0; x < COLS; x++) {
      const [r, g, b] = px(x, y);
      const d = Math.hypot(r - bg[0], g - bg[1], b - bg[2]);
      if (d < 42) {
        line += " ";
        continue;
      }
      const density = Math.min(1, (d - 42) / 150);
      line += RAMP[Math.round((1 - density) * (RAMP.length - 1))];
    }
    lines.push(line.replace(/\s+$/, ""));
  }
  return lines.join("\n");
}

const frames = [];
for (const f of files) frames.push(await toAscii(f));

if (previewOnly) {
  console.log(`frames=${frames.length} ${COLS}x${ROWS}`);
  console.log("\n===== frame ~middle =====");
  console.log(frames[Math.floor(frames.length / 2)]);
} else {
  const out =
    `// AUTO-GENERATED by scripts/gen-charm-from-video.mjs — ${frames.length} ASCII frames\n` +
    `// converted from Alan's own generated video (each frame rasterised to text).\n` +
    `export const charmCols = ${COLS};\n` +
    `export const charmFrames: string[] = [\n` +
    frames.map((fr) => "  `\n" + fr + "\n`").join(",\n") +
    `\n];\n`;
  writeFileSync("src/content/charmFrames.ts", out);
  console.log(`wrote src/content/charmFrames.ts (${frames.length} frames, ${COLS}x${ROWS})`);
}
