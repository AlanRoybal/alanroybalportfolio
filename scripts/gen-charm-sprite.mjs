import fs from "fs";
const txt = fs.readFileSync("src/content/charmFrames.ts", "utf8");
const frames = [];
const re = /`([\s\S]*?)`/g; let m;
while ((m = re.exec(txt))) frames.push(m[1]);
const COLS = 150;
function downsample(frame, targetW, hMul) {
  const rows = frame.replace(/\n$/,"").split("\n").map(r => r.replace(/\s+$/,""));
  const H = rows.length;
  const grid = rows.map(r => { const a=r.split(""); while(a.length<COLS) a.push(" "); return a; });
  const bw = COLS/targetW, bh = bw*hMul;
  const targetH = Math.max(1, Math.round(H/bh));
  const ramp = " .:-=+*#%@";
  const out=[];
  for(let ty=0;ty<targetH;ty++){ let line="";
    for(let tx=0;tx<targetW;tx++){ let f=0,t=0;
      const x0=Math.floor(tx*bw),x1=Math.floor((tx+1)*bw),y0=Math.floor(ty*bh),y1=Math.floor((ty+1)*bh);
      for(let y=y0;y<y1&&y<H;y++)for(let x=x0;x<x1&&x<COLS;x++){t++;if((grid[y]?.[x]||" ")!==" ")f++;}
      const d=t?f/t:0; line+=ramp[Math.min(ramp.length-1,Math.round(d*(ramp.length-1)))]; }
    out.push(line.replace(/\s+$/,"")); }
  while(out.length&&out[0].trim()==="") out.shift();
  while(out.length&&out[out.length-1].trim()==="") out.pop();
  const minIndent=Math.min(...out.filter(l=>l.trim()).map(l=>l.length-l.trimStart().length));
  return out.map(l=>l.slice(minIndent).replace(/\s+$/,""));
}
const lines = downsample(frames[0], 30, 1.7);
const W = Math.max(...lines.map(l=>l.length));
const padded = lines.map(l => l.padEnd(W));
const body = padded.map(l => l).join("\\n");
const file = `// AUTO-GENERATED by scripts/gen-charm-sprite.mjs — a small isolated ASCII\n// sprite of "the charm" (downsampled from charmFrames[0]), used by ScrollCharm\n// as a flying mascot that traverses the page on scroll.\nexport const charmSpriteCols = ${W};\nexport const charmSpriteRows = ${padded.length};\nexport const charmSprite = ${JSON.stringify(padded.join("\n"))};\n`;
fs.writeFileSync("src/content/charmSprite.ts", file);
console.log("Wrote src/content/charmSprite.ts —", W+"x"+padded.length);
console.log(padded.join("\n"));
